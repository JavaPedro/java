    public void verificaJogadorAouB(){
        if(joga==true && jogb==true) {
            this.op = 1;
            joga = false;
            jogb = true;
            tela_txt.setText("Jogue, player O");
        }
        else if(joga==true && jogb ==false) {
            this.op = 1;
            joga = false;
            jogb = true;
            tela_txt.setText("Jogue, player O");
        }
        else {
            this.op = 2;
            joga = true;
            jogb = false;
            tela_txt.setText("Jogue, player X");
        }
    }
